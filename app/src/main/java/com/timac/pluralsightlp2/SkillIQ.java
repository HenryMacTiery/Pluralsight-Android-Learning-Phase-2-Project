package com.timac.pluralsightlp2;

public class SkillIQ {
    private String name;
    private int score;
    private String country;
    private String badgeUrl;


    public String getName() {
        return name;
    }

    public int getScore() {
        return score;
    }

    public String getCountry() {
        return country;
    }

    public String getBadgeUrl() {
        return badgeUrl;
    }
}
